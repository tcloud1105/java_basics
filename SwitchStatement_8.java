public class SwitchStatement_8 {
    public static void main(String[] args){
        int age=4;

        switch (age){
            case 1:
                System.out.println("you can crawl");
                break;
            case 2:
                System.out.println("you can smile");
                break;
            case 3:
                System.out.println("you can dance");
                break;
            default:
                System.out.println("you can't do anything");

        }
    }
}
